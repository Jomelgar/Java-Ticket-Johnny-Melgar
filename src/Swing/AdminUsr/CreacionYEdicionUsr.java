    private void BtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtConfirmarActionPerformed
        // TODO add your handling code here:
        if(camposvacios() == true){
            int edad = Integer.parseInt(TextoEdad.getText());
            switch(tipo){
                case 1:
                    Administrador usuario1 = new Administrador(edad,
                            TextoNombre.getText(), TextoContra.getText(),
                            TextoUsuario.getText());
                    if(Caso == true){
                        if(Inicializador.GestorUsuarios.agregarUsuario(usuario1) == true){
                        JOptionPane.showMessageDialog(null,
                                "Usuario creado con exito.");
                        this.dispose();
                        }else{
                            JOptionPane.showMessageDialog(null,
                                    "ERROR: Ese nombre de usuario ya existe.");
                        }
                    }else{
                        if(Inicializador.usuario.equals(usuario1)){
                            Inicializador.usuario = usuario1;
                        }
                        Inicializador.GestorUsuarios.EditarUsuario(usuario1, pos);
                        JOptionPane.showMessageDialog(null,
                                    "Usuario editado con exito.");
                        this.dispose();
                    }
                    break;
                case 2:
                    DeContenido usuario2 = new DeContenido(edad,
                            TextoNombre.getText(), TextoContra.getText(),
                            TextoUsuario.getText());
                    if(Caso == true){
                        if(Inicializador.GestorUsuarios.agregarUsuario(usuario2) == true){
                        JOptionPane.showMessageDialog(null,
                                "Usuario creado con exito.");
                        this.dispose();
                        }else{
                            JOptionPane.showMessageDialog(null,
                                    "ERROR: Ese nombre de usuario ya existe.");
                        }
                    }else{
                        if(Inicializador.usuario.equals(usuario2)){
                            Inicializador.usuario = usuario2;
                        }
                        Inicializador.GestorUsuarios.EditarUsuario(usuario2, pos);
                        JOptionPane.showMessageDialog(null,
                                    "Usuario editado con exito.");
                        this.dispose();
                    }
                    break;
                case 3:
                    Limitado usuario3 = new Limitado(edad,TextoNombre.getText()
                            ,TextoContra.getText(),
                            TextoUsuario.getText());
                    if(Caso == true){
                        if(Inicializador.GestorUsuarios.agregarUsuario(usuario3) == true){
                        JOptionPane.showMessageDialog(null,
                                "Usuario creado con exito.");
                        this.dispose();
                    }else{
                            JOptionPane.showMessageDialog(null,
                                    "ERROR: Ese nombre de usuario ya existe.");
                        }
                    }else{
                        if(Inicializador.usuario.equals(usuario3)){
                            Inicializador.usuario = usuario3;
                        }
                        Inicializador.GestorUsuarios.EditarUsuario(usuario3, pos);
                        JOptionPane.showMessageDialog(null,
                                    "Usuario editado con exito.");
                        this.dispose();
                    }
                    break;        
            }
        }else if(camposvacios() == false){
            JOptionPane.showMessageDialog(null,
                                "ERROR: Tiene algun campo vac칤o.");
        }else{
            JOptionPane.showMessageDialog(null,
                                "ERROR: No agrego una edad v치lida");
        }    
    }//GEN-LAST:event_BtConfirmarActionPerformed

    private void TextoEdadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TextoEdadKeyTyped
        // TODO add your handling code here:
        int tecla = evt.getKeyChar();
        boolean valido = tecla >= 48 && tecla <= 57;

        if(!valido){
            evt.consume();
        }
    }//GEN-LAST:event_TextoEdadKeyTyped
 
    public boolean camposvacios(){
        return !TextoContra.getText().trim().isEmpty() && !TextoNombre.getText().trim().isEmpty() &&
                !TextoUsuario.getText().trim().isEmpty() && !TextoEdad.getText().trim().isEmpty();
    }

